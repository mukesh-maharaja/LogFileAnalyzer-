    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        // TODO add your handling code here:
        String userEmail = jTextField1.getText().trim();
        String userMessage = jTextArea1.getText().trim();

        if (userEmail.isEmpty() || userMessage.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Both fields are required.", "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!userEmail.matches("^[\\w.%+-]+@[\\w.-]+\\.[a-zA-Z]{2,6}$")) {
            JOptionPane.showMessageDialog(null, "Invalid email address.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            EMail.sendEmail(userEmail, userMessage);
            jTextField1.setText("");
            jTextArea1.setText("");
        }
    }



